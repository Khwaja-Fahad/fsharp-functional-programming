// Demonstrating let bindings and immutability

// Basic let binding
let number = 10
printfn "Original number: %d" number

// Explicit typing
let age:int = 22
let name:string = "Muhammad Fahad"

printfn "Name: %s, Age: %d" name age

// Basic arithmetic
let a = 12
let b = 5

let sum = a + b
let difference = a - b
let product = a * b
let division = a / b
let remainder = a % b

printfn "Sum: %d" sum
printfn "Difference: %d" difference
printfn "Product: %d" product
printfn "Division: %d" division
printfn "Remainder: %d" remainder