// =======================
// F# Data Types Examples
// =======================

// Integer
let age:int = 22
printfn "Age: %d" age

// Float
let height:float = 5.9
printfn "Height: %.2f" height

// String
let name:string = "Muhammad Fahad"
printfn "Name: %s" name

// Boolean
let isStudent:bool = true
printfn "Is Student: %b" isStudent


// Type Inference (F# detects automatically)
let score = 95
let temperature = 36.6
let message = "F# is powerful"

printfn "Score: %d" score
printfn "Temperature: %f" temperature
printfn "Message: %s" message